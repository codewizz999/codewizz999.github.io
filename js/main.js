function closenav(){
  var x = document.getElementById("side-nav");
  if (x.style.display === "none") {
  
      x.style.display="block";
      
    } else {
      x.style.display="none";
    
    }
}
function opennav(){
  var x = document.getElementById("side-nav");
  
  if (x.style.display === "none") {
  
      x.style.display="block";
      
    } else {
      x.style.display="none";
    
    }

}
